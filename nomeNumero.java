/*Leia o nome um número do usuário um número N e escreva o nome dele na tela N
vezes. */
public class nomeNumero {
    public static void main(String[] args) {
        String nome = "João";
        int n = 10;
        for (int i = 0; i < n; i++) {
            System.out.println(nome);
        }
    }
}
